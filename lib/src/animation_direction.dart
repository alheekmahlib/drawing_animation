/// Animation direction enum - تعداد اتجاه الأنميشن
/// Defines the direction of the animation - يحدد اتجاه الأنميشن
enum AnimationDirection {
  /// Animation starts from left to right - الأنميشن يبدأ من اليسار إلى اليمين
  leftToRight,

  /// Animation starts from right to left - الأنميشن يبدأ من اليمين إلى اليسار
  rightToLeft,

  /// Animation follows the original path order - الأنميشن يتبع ترتيب المسار الأصلي
  original,
}
